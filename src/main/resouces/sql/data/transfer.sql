CREATE TABLE TRANSFER
(
    ID            BIGSERIAL NOT NULL,
    SOURCE_ID     BIGINT NOT NULL,
    TARGET_ID     BIGINT NOT NULL,
    AMOUNT        FLOAT8 NOT null CONSTRAINT C_POSITIVE_AMOUNT CHECK (AMOUNT > 0),
    TRANSFER_TIME TIMESTAMP NOT NULL,
    PRIMARY       KEY (ID),
    CONSTRAINT    FK_TRANSFER_SOURCE_ID FOREIGN KEY (SOURCE_ID) REFERENCES ACCOUNT,
    CONSTRAINT    FK_TRANSFER_TARGET_ID FOREIGN KEY (TARGET_ID) REFERENCES ACCOUNT
)